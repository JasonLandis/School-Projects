			<h1 class="font-weight-light"><strong>Chemical Overview</strong></h1>
			<p>
				<strong>
				Before developing your own film, it is important to understand the chemical processes that occur during development. To this end, I will give a brief overview of process from start to finish.
				Photographic film is a piece of gelatin coated in a material called silver halide. When you take a picture, the silver halide reacts to the light, creating what is called a latent image. The process of developing film starts with this 'latent image' and ends with a fully visible negative on the piece of film. The film is immersed in different chemicals for a set amount of time, then washed with water and dried. The first chemical, called 'developer', converts the silver halide into visible metallic silver. The parts of the film that have been exposed to light form metallic silver while the parts not exposed to any light do not react with the developer and remain silver halide. After the developer, the film is placed in another chemical called a 'stop bath'. The function of the stop bath is to neutralize any developer left on the film, stopping the development process and making sure too much metallic silver is not formed. Then, the film is placed in a final chemical called 'fixer'. The fixer removes any silver halide left on the image that did not react with the developer. Once the film has been placed in all three chemicals, the development process is finished. The final product is a negative image - an image where the lightest parts appear dark, and the darkest parts appear bright.</strong>
			</p>		
