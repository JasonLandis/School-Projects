				<h1 class="font-weight-light"><strong>Push/Pull Processing</strong></h1>
				<p>
					<strong>A significant advantage of processing film at home is the ability to 'push' or 'pull' your film. If, after finishing a roll of film, you realized your light meter was set incorrectly, you can simply increase the time you put the film in the developer for. If you have underexposed the photo (made it too dark), you can leave the photo in the developer for longer. This causes the metallic silver to grow more, and brightens the image to compensate for the image being too dark. If you have overexposed(made it too bright), you can do the opposite. More specific instructions for pushing and pulling can be found online at <a href="https://www.digitaltruth.com/devchart.php">The Massive Dev Chart</a> or is sometimes mentioned on bottles of developer.</strong>
				</p>
