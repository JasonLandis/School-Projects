					<p>
					<strong>
					The first step of home developing is often the most difficult. In order to prepare the film for developing, you must take it out of its canister, roll it onto the developing spool, and put it in the developing tank. Sounds easy, right? Here's the catch: you must do this all inside of your changing bag. It is essential that NO LIGHT shines on the film until after it comes out of the stop bath. To accomplish this, you must place your film canister, a can opener, and the developing tank and reels inside of the changing bag and zip it closed. Put your arms through the light-tight sleeves, use the can opener to pry open the film canister, and begin rolling the film onto the spool. Below is a short video of me loading some already-developed film into the tank outside of the changing bag so you can see what you have to do. Note that the film I loaded is not an entire roll; I cut my rolls to store in laminated paper. A roll of 35mm is about six times as long as the strip I loaded.</strong>
