                            	<p class="card-text">
									Finally, you must wash your film thouroughly with water. I leave the developing tank under running water for at least 5 minutes. Washing is important because it increases the longevity of your negatives and makes sure all the chemicals are removed from the film.
									After washing, hang up to dry for a few hours, and your negatives are finished! Once dried, I cut my film and sleeve it in archival sleeves for optimal preservation - those will be included in the catalog as well.
