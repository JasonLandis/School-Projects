								<h2 class="card-title"><strong>The Developer</strong></h2>
								<p class="card-text">
									Once your film has been loaded, it's time to pour in the developer. Open the top of the developing tank and pour in the developer until it is full. The developing tank is specially designed to allow pouring of liquids without exposing the film to light, so this is perfectly safe. Developer is often stored in concentrates; the developer may need to be diluted to function properly. A handy website with dilution ratios and developing times that I like to use is <a href="https://www.digitaltruth.com/devchart.php">The Massive Dev Chart</a> - simply search the film you used and the developer you are using,  and it will give you a precise time to leave the developer in for. It is important to agitate (invert) the developing tank every 30 seconds four times or so to make sure the chemicals are distributed evenly on the film. After you have finished with the developer, you can dump it into the sink - most developers are single use.
								</p>
							</div>                        
						</div>
					</div>
					<div class="col-md-4 mb-5">
                    	<div class="card h-100">
                        	<div class="card-body">
                            	<h2 class="card-title"><strong>The Stop Bath and Fixer</strong></h2>
                            	<p class="card-text">
									After pouring out the developer, it's time to pour in the stop bath. The same rules used with the developer apply - the mixture may be diluted and time is dependent on the type of stop bath you buy. Normally, it is much shorter than developer. Remember to agitate every 30 seconds. The stop bath can be reused, so if you wish to do so pour your stop bath into a 1000ml amber glass bottle for storage. Amber glass lets in less light than normal glass, which increases the working life of the stop bath. After this step you can expose your film to light if you wish. Then it's time to pour in the fixer. Once again, the same rules apply - dilute, pour, agitate, and then pour out - the fixer can be reused too.
								</p>
