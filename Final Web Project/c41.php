				<h3 class="font-weight-light"><strong id="film">Mixing Chemicals</strong></h3>
				<p>
					<strong>While black and white processing chemicals come in liquid concentrates, most C-41 chemicals come in powdered form. Within the kit will be instructions on how to mix the chemicals, but most kits involve mixing the powders into heated water, then pouring each of the three chemicals into their own amber glass bottles. All three chemicals are reusable, so C-41 is especially economical. Once you have the necessary equipment, you can often process rolls at a cost of less than $2 per roll!</strong>
