				<h3 class="font-weight-light"><strong>Differences From Black and White Processing</strong></h3>
				<p>
					<strong>There are a few key differences when it comes to color and black and white film. While color film is also coated in silver halide, the silver halide is coupled to dyes. When the developer causes the silver halide to grow, it also causes the colored dyes to grow. Instead of a stop bath, the C-41 process uses a chemical called 'blix' - a combination of bleach and fixer. The bleach washes away the metallic silver and the fixer washes away the silver halide, leaving only the dyes on the film. The film is then immersed in a stablizing chemical that reduces degradation of the film. Like black and white film, film processed with C-41 chemistry results in a negative image. In addition to the chemical differences, there are also some differences in what you must do to process the film. The most important is that color chemicals must be temperature controlled - the developing tank must be kept at precisely 39 degrees celsius. Even minor variations from this number can cause color or density shifts in your film. Don't worry, though - there is a simple piece of equipment that will fix this issue.</strong>
				</p>
