                            <h2 class="card-title"><strong>Loading the Film</strong></h2>
                            <p class="card-text">
								The process of loading the film is exactly the same as it is in black and white processing. For a detailed explanation and short video on the topic, see the guide on <a href="b&w.html">Black & White Developing</a>. Once your film has been loaded, place the three chemicals in water that is being heated to 39 degrees celsius by the sous vide cooker. The cooker will have a thermometer built in so you can tell when the water is at the right temperature. It is critical to keep both the developing tank and the chemicals submerged in the bath continuously. Only remove the chemicals or tank when you are pouring the chemicals or agitating the developing tank. Before pouring in your developer, color film must be pre-soaked in hot water. This heats the film to the correct temperature and removes	protective layers to the film to prepare it for processing. Most developing kits call for the film to be soaked in hot water for at least a minute.
							</p>
                        </div>                        
                    </div>
            	</div>
				<div class="col-md-4 mb-5">
					<div class="card h-100">
						<div class="card-body">
							<h2 class="card-title"><strong>Using Chemicals</strong></h2>
							<p class="card-text">
								After the pre-soak, it's now time to pour in the developer. Open the top of the developing tank and pour in the developer until it is full. The developing tank is specially designed to allow pouring of liquids without exposing the film to light, so this is perfectly safe. Once again, the process is very similar to black and white processing. Leave the developing tank in water and only take it out every 30 seconds to agitate. The chemical kit you purchased will have more detailed instructions for exactly how long to leave the film in the developer. Remember, C-41 developer can be reused, so make sure to pour it back into an amber glass bottle for reuse. Then pour in the blix. Again, leave it in the water while inverting every 30 seconds. After this step, you can safely expose the film to light. The stabilizer is the last chemical needed. The stabilizer can be done at room temperature, so no need to leave the developing tank in the water.
							</p>
						</div>
					</div>                        
				</div>
				<div class="col-md-4 mb-5">
                    <div class="card h-100">
                        <div class="card-body">
                            <h2 class="card-title"><strong>Wash/Dry</strong></h2>
                            <p class="card-text">
								Finally, you must wash your film thouroughly with water. I leave the developing tank under running water for at least 5 minutes. Washing is important because it increases the longevity of your negatives and makes sure all the chemicals are removed from the film. After washing, hang up to dry for a few hours, and your negatives are finished! Once dried, I cut my film and sleeve it in archival sleeves for optimal preservation - those will be included in the catalog as well!
